public class Calculate {

    /**
     * 加法
     *
     * @param a
     * @param b
     * @return
     */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * 减法
     *
     * @param b 被减数
     * @param a 减数
     * @return
     */
    public int sub(int b, int a) {
        return b - a;
    }
}
